#include "menu.h"
#include <iostream>

MENU showMenu()
{
	while (1)
	{
		// 메뉴 보여주기
		std::cout << "\n------ 메뉴 ------\n";
		std::cout << "1. 학생 성적 추가\n";
		std::cout << "2. 전체 성적 보기\n";
		std::cout << "Q. 프로그램 종료\n";
		std::cout << "------------------\n\n";
		std::cout << "원하는 작업의 번호를 입력하세요 : ";

		char menuNum;
		std::cin >> menuNum;

		switch (menuNum)
		{
		case '1':
			return MENU::MENU_ADD_STUDENT;
		case '2':
			return MENU::MENU_SHOW_ALL;
		case 'q':
		case 'Q':
			return MENU::MENU_QUIT;
		default:
			std::cout << "\n올바른 입력 값을 선택하세요" << "\n";
			break;
		}
	}

	return MENU::MENU_QUIT;
}